    private static boolean sendMessage(java.lang.String message, java.lang.String coordenadas, java.lang.String owner, java.lang.String token, java.lang.String status, java.lang.String macAddress) throws WebServiceException {
        webservices.ContactManager_Service service = new webservices.ContactManager_Service();
        webservices.ContactManager port = service.getContactManagerPort();
        try {
            return port.sendMessage(message, coordenadas, owner, token, status, macAddress);
        } catch (WebServiceException e) {

        }
        return false;
    }

    private static String getLocation(java.lang.String macAddress) throws WebServiceException {

        try {
            webservices.ContactManager_Service service = new webservices.ContactManager_Service();
            webservices.ContactManager port = service.getContactManagerPort();

            return port.getLocation(macAddress);
        } catch (WebServiceException e) {

        }
        return null;
    }

